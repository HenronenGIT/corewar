char	*ft_strcat(char *dest, const char *src)
{
	char	*sum;

	sum = dest;
	while (*sum != '\0')
		sum++;
	while (*src != '\0')
	{
		*sum = *src;
		src++;
		sum++;
	}
	*sum = '\0';
	return (dest);
}
